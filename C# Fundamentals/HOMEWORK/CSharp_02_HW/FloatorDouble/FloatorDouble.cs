using System;


class FloatOrDouble
{
    static void Main(string[] args)
    {
        double firstDoubleVar = 34.567839023;
        float firstFloatVar = 12.345f;
        double secondDoubleVar = 8923.1234857;
        float secondFloatVar = 3456.091f;

        Console.WriteLine("Variable firstDoubleVar: {0}", firstDoubleVar);
        Console.WriteLine("Variable firstFloatVar: {0}", firstFloatVar);
        Console.WriteLine("Variable secondDoubleVar: {0}", secondDoubleVar);
        Console.WriteLine("Variable secondFloatVar: {0}", secondFloatVar);
    }
}